''''7. Створіть масив А [1..12] за допомогою генератора випадкових чисел з
елементами від -20 до 10 і виведіть його на екран. Замініть всі від’ємні елементи
масиву числом 0.'''

import numpy as np
import random
a=np.array([random.randint(-20,11) for i in range(12)])#створення масиву
print(a)
a_new=a
for g in range(len(a_new)):#прохід по елементах масиву
    if a_new[g]<0:#перевірка умови
        a[g]=0#заміна на нуль
print(a_new)