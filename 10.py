'''10. Дані про температуру повітря за декаду листопада зберігаються в масиві.
Визначити, скільки разів температура опускалася нижче -10 градусів.'''

import numpy as np
import random
b=[random.randint(-11,7) for i in range(10)]
a=np.array(b)#створення масиу
print('Погода у листопаді з 1 по 10 число:')
print(a)
k=0
for i in a:# прохід по елементах масиву
    if i<-10:# перевірка умови
        k+=1
print(f'Погода нижче -10 градусів: {k} раз')