'''6. Створіть масив А [1..8] за допомогою генератора випадкових чисел з
елементами від -10 до 10 і виведіть його на екран. Підрахуйте кількість від’ємних
елементів масиву.'''

import numpy as np
import random
b=[random.randint(-10,10) for i in range(8)]
a=np.array(b)#створення масиву
print(a)
k=0#лічильник
for i in a:#проходження по елементах масиву
    if i < 0:#перевірка умови
        k+=1
print(f'Від’ємних чисел: {k}')