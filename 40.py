'''40. Обчислити суму парних елементів одновимірного масиву до першого
зустрінутого нульового елемента.'''

import numpy as np
a=[1,4,16,7,3,0,2,4,6,6,0,3,4,8,0]
a=a[:a.index(0)]#обрізаємо список до першого нуля
b=[]
for i in a:
    if i%2==0:
        b.append(i)
a_new=np.array(b)
print('Мавсив:',a)
print('Парні елементи до першого нуля:',a_new)
print(f'Їх сума:{a_new.sum()}')