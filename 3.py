'''3. Створіть масив з п'яти прізвищ і виведіть їх на екран стовпчиком,
починаючи з останнього.'''

import numpy as np
x=list()
for i in range(1,6):#введення прізвищ
    a=input(f'Прізвище{i}:')
    x.append(a)#додавання їх до списку
x.sort(reverse=True)#сортування у зворотньому порядку
b=np.array(x).reshape(5,1)#формативне представлення
print(b)