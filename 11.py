'''11. Дані про температуру води на Чорноморському узбережжі за декаду
вересня зберігаються в масиві. Визначити, скільки за цей час було днів, придатних для
купання.'''
import numpy as np
import random
b=[random.randint(2,30) for i in range(10)]
a=np.array(b)#створення масиву
print('Температура води з 1 по 10 вересня:')
print(a)
k=0#лічильник
for g in a:#прохід по елементах масиву
    if 22<g<24:#перевірка умови
        k+=1
print(f'Днів придатних для купання: {k}')