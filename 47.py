'''47. У лінійному масиві знайти максимальний елемент. Вставте порядковий
номер елемента за ним, пересунувши всі залишилися на одну позицію вправо.'''

import random
a=[random.randint(1,10) for i in range(5)]
print(a)
b=max(a)
print('Максимаьний елемент:',b)
c=a.index(max(a))
print('Його індекс:',c)
a[c+1:c+2]=[c]
print('Новий список:',a)