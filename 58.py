'''58. Дан одновимірний масив цілих чисел. Знайдіть, скільки разів в ньому
повторюється найчастіше число.'''

x=[]#список кількості входжень кожного елемента (a[i]) в список (a)
a=[1,1,2,4,5,5,5,5,6,7]
print(a)
for i in range(len(a)):
    x.append(a.count(a[i]))
print('Найчастіше чило:',a[x.index(max(x))])#виводимо число по тому ж індексу що має max-значення в спписку (x)
print(f'Зустрічаєть: {x[x.index(max(x))]} раз(и)')#вмводимо (x) по індексу максимально значення