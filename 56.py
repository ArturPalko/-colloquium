'''56. Якщо в одновимірному масиві є три поспіль однакових елемента, то
змінній r привласнити значення істина.'''
k=0#лічильник кількості збігів виконання умови
r=False
a=[1,2,4,2,4,2,2,2]
print(a)
for i in range(len(a)-1):#не беремо (і) по останньому індексу (а),щоб не було виходу за межі
    if a[i]==a[i+1]:#перевіряємо елемент по індексу (і) з наступним
        k+=1
    if k==2:
     r=True
print(r)
